#include <bits/stdc++.h>
using namespace std;
#define ll long long

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ll t;
    cin >> t;

    while (t--) {
        int n ,b;
        cin>>n>>b;

        ll ans = LLONG_MAX ;


        for(int i =0; i<33;i++){

            if(i==0 && b==1)continue;

            ll val = i + b;
            ll cnt =0;
            ll x = n;

            while(x>0){
                cnt++;
                x = x/val;
            }

            ans = min(ans , cnt+i);

        }

        cout<<ans<<endl;
       
    }
    return 0;
}
