#include <bits/stdc++.h>
using namespace std;
#define ll long long

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ll t;
    cin >> t;

    while (t--) {
        ll n;
        cin >> n;
        vector<ll> vec(n);
        for (int i = 0; i < n; i++) cin >> vec[i];
        vector<ll> dp(n+1 , 1e9);
        // dp[i] = minimum number of operations required to make array from i to n beautiful
        dp[n] = 0;
        dp[n-1] = 1;
        for(int i = n-2 ; i >= 0 ; i--) {
            dp[i] = min(dp[i+1] + 1 , ((i + vec[i] + 1) <= n ? dp[i + vec[i] + 1] : n-i));
        }
        cout << dp[0] << '\n';
    }
    return 0;
}
