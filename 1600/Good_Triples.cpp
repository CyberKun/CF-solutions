#include <bits/stdc++.h>
using namespace std;
#define ll long long

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ll t;
    cin >> t;

    while (t--) {
        ll n;
        cin >> n;
        vector<int> arr(28, 0);

        for (int a = 0; a <= 9; a++){
            for (int b = 0; b <= 9; b++){
                for (int c = 0; c <= 9; c++){
                    arr[a + b + c]++;
                }
            }
        }
        ll ans = 1;
        while(n) {
            ans *= arr[n%10];
            n/=10;
        }
        cout << ans << '\n';
    }
    return 0;
}
