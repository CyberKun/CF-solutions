#include <bits/stdc++.h>
using namespace std;
#define ll long long

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ll t;
    cin >> t;

    while (t--) {
        ll n;
        cin >> n;
        string s;
        cin >> s;
        for(int i = 0 ; i < n - 2 ; i++) {
            if(s[i] == '1' && s[i + 1] == '0' && s[i + 2] == '1') {
                s[i + 1] = '1';
                i ++;
            }
        }
        ll ct = 0;
        for(int i = 0 ; i < n ; i++) if(s[i] == '1') ct++;
        ll ct1 = 0;
        for(int i = 0 ; i < n ; i++) {
            if(s[i] == '0') ct1++;
            else if(i < n - 2 && (s[i] == '1' && s[i + 1] == '1' && s[i + 2] == '1')) {
                ct1++;
                i++;
            }
        }
        cout << n - ct1 << ' ' << ct << '\n';
    }
    return 0;
}
