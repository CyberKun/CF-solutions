#include <bits/stdc++.h>
using namespace std;
#define ll long long

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    ll t;
    cin >> t;

    while (t--)
    {
        ll n, m, l;
        cin >> n >> m >> l;

        vector<ll> a(n);
        for (auto &x : a)
            cin >> x;

        ll ans = (l + m - 1) / m; // ceil(l/m)

        for (auto x : a)
            ans = max(ans, l - x);

        cout << ans << '\n';
    }

    return 0;
}